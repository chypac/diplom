"""
Модуль проверки системной конфигурации

Этот модуль предоставляет функции для:
1. Проверки доступных сетевых интерфейсов
2. Поиска файлов моделей машинного обучения

Используется для предварительной проверки системы перед запуском
основного приложения обнаружения аномалий.

Требования:
    - psutil: для получения информации о сетевых интерфейсах
    - os: для работы с файловой системой

Автор: [Агафонов Артём]
Дата создания: 2024
"""

import psutil
import os

def list_network_interfaces():
    """
    Отображает список всех активных сетевых интерфейсов в системе.
    
    Выводит информацию о каждом найденном сетевом интерфейсе:
    - Имя интерфейса
    - Статус (активен/неактивен)
    
    Returns:
        None
    """
    print("\nДоступные сетевые интерфейсы:")
    print("-" * 50)
    for iface, stats in psutil.net_if_stats().items():
        if stats.isup:
            print(f"Интерфейс: {iface}")
            print(f"Статус: {'Активен' if stats.isup else 'Неактивен'}")
            print("-" * 50)

def find_model_file():
    """
    Ищет файлы моделей с расширением .pth в текущей директории.
    
    Выводит информацию о каждом найденном файле модели:
    - Имя файла
    - Полный путь к файлу
    
    Returns:
        None
    """
    print("\nПоиск файлов модели (.pth):")
    print("-" * 50)
    current_dir = os.path.dirname(os.path.abspath(__file__))
    for file in os.listdir(current_dir):
        if file.endswith('.pth'):
            print(f"Найден файл модели: {file}")
            print(f"Полный путь: {os.path.join(current_dir, file)}")

if __name__ == "__main__":
    print("Проверка системной конфигурации")
    print("=" * 50)
    list_network_interfaces()
    find_model_file()
